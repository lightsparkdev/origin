// Aspect ratios from Figma (width/height at 80px height)
const ASPECT_RATIOS: Record<Variant, Record<Weight, number>> = {
  logo: { regular: 434 / 80, light: 429 / 80 },
  logomark: { regular: 128 / 80, light: 127 / 80 },
  wordmark: { regular: 374 / 80, light: 374 / 80 },
};

export const Logo = React.forwardRef<SVGSVGElement, LogoProps>(
  function Logo(props, forwardedRef) {
    const {
      variant = 'logo',
      weight = 'regular',
      height = 80,
      className,
      ...svgProps
    } = props;

    // Wordmark only has regular weight
    const effectiveWeight = variant === 'wordmark' ? 'regular' : weight;
    const aspectRatio = ASPECT_RATIOS[variant][effectiveWeight];
    const width = height * aspectRatio;

    const svgContent = getSvgContent(variant, effectiveWeight);

    return (
      <svg
        ref={forwardedRef}
        role="img"
        className={clsx(styles.root, className)}
        width={width}
        height={height}
        viewBox={svgContent.viewBox}
        fill="none"
        xmlns="http://www.w3.org/2000/svg"
        {...svgProps}
      >
        {svgContent.paths}
      </svg>
    );
  }
);

