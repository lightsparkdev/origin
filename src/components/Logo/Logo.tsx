type Brand = 'lightspark' | 'grid';
type Variant = 'logo' | 'logomark' | 'wordmark';
type Weight = 'regular' | 'light';

export interface LogoProps extends Omit<React.SVGProps<SVGSVGElement>, 'children' | 'color'> {
  /** Brand. Grid only supports logo and logomark variants, no weight options. */
  brand?: Brand;
  /** Logo variant. Note: Grid brand does not support wordmark. */
  variant?: Variant;
  /** Stroke weight. Only applies to Lightspark brand. Wordmark only has regular weight. */
  weight?: Weight;
  /** Height in pixels (width scales proportionally). Default: 24 */
  height?: number;
  /** 
   * Logo color. Accepts any CSS color value or token.
   * @default 'var(--surface-inverted)'
   */
  color?: string;
  /** Required for accessibility. Describes the logo for screen readers. */
  'aria-label': string;
}

// Aspect ratios from Figma (width/height from viewBox)
const LIGHTSPARK_ASPECT_RATIOS: Record<Variant, Record<Weight, number>> = {
  logo: { regular: 130 / 24, light: 129 / 24 },
  logomark: { regular: 38.169 / 24, light: 37.9355 / 24 },
  wordmark: { regular: 112 / 24, light: 112 / 24 },
};

const GRID_ASPECT_RATIOS: Record<'logo' | 'logomark', number> = {
  logo: 90 / 24,
  logomark: 24 / 24,
};

export const Logo = React.forwardRef<SVGSVGElement, LogoProps>(
  function Logo(props, forwardedRef) {
    const {
      brand = 'lightspark',
      variant = 'logo',
      weight = 'regular',
      height = 24,
      color,
      className,
      style,
      ...svgProps
    } = props;

    // Grid only supports logo and logomark, fallback to logo for wordmark
    const effectiveVariant = brand === 'grid' && variant === 'wordmark' ? 'logo' : variant;
    // Wordmark only has regular weight, Grid has no weight variants
    const effectiveWeight = variant === 'wordmark' || brand === 'grid' ? 'regular' : weight;
    
    const aspectRatio = brand === 'grid'
      ? GRID_ASPECT_RATIOS[effectiveVariant as 'logo' | 'logomark']
      : LIGHTSPARK_ASPECT_RATIOS[effectiveVariant][effectiveWeight];
    const width = height * aspectRatio;

    const svgContent = getSvgContent(brand, effectiveVariant, effectiveWeight);

    return (
      <svg
        ref={forwardedRef}
        role="img"
        className={clsx(styles.root, className)}
        style={{ color, ...style }}
        width={width}
        height={height}
        viewBox={svgContent.viewBox}
        fill="none"
        xmlns="http://www.w3.org/2000/svg"
        {...svgProps}
      >
        {svgContent.paths}
      </svg>
    );
  }
);

if (process.env.NODE_ENV !== 'production') {
  Logo.displayName = 'Logo';
}

